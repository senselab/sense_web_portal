<h4 class="media-heading">Android Application Synchronization</h4>
<small>
<a href="projects/appsync.pdf"><span class="fa fa-file-pdf-o"></span></a>
</small></br/>
隨著智慧裝置的普及，每個手上的智慧裝置也越來越多，當手上的智慧裝置越來越多時，我們會希望能把所有的 app data（app data 為 app 在/data/data 下所存放的屬於該 app 的系統資料，內容包括了該 app 的設定、有關該 app 的所有的資料。ex: 若該 app 是一款手機遊戲，在該目錄下所存放的 app data 可能就包括目前的遊戲紀錄。若該 app 是一款通訊軟體，該 app data 就可能存放著通訊歷史紀錄等。）都能夠保存下來，並且讓不同裝置間的 app 資料能夠透過雲端或者直接在本地端進行同步，能對使用者帶來極大的便利性。<br/>
而目前 Android 上，對於 app data 的備份的解決方法也有一些工具可以使用，但是目前的解決辦法都需要使用者對他們 Android device 進行 root 的動作，對於一般的使用者來說，這是一個非常不友善而且危險的方法，幾乎所有的手機廠都不支持使用者進行 root 的動作，一旦有 root 的行為，該裝置就會失去保固，這是一般使用者所不樂見的，所以我們希望能夠提供一個不需要 root 的方法進行備份、同步、分享的動作。
