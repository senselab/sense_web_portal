<h4 class="media-heading">Hybrid-mode Information Flow Tracking</h4>
<small>
<a href="projects/2018_hit.pdf"><span class="fa fa-file-pdf-o"></span></a>
</small></br/>
Due to increasing complexity in security attacks, it is no longer sufficient to rely on generic network-level and system-level events for attack detection. We propose the hybrid-mode information flow tracking (HIT) system to reveal application-level events by integrating static information analysis (IFA) and dynamic information flow tracking (DIFT) into the applications. Preliminary results indicate the effectiveness of the approach in detecting sensitive data leakage with a modest performance overhead.
